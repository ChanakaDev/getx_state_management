// product class
class Product {
  // class variables (attributes)
  final int id;
  final String productName;
  final String productImage;
  final String productDescription;
  final double price;

  // constructor
  Product({
    required this.id,
    required this.productName,
    required this.productImage,
    required this.productDescription,
    required this.price,
  });
}
